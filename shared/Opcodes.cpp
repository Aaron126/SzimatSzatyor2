#include "Opcodes.h"

void OpcodeTable::ValidateAndSetOpcode(unsigned int opcodeNumber, char const* name)
{
    if (opcodeNumber == 0xBADD)
        return;

    if (GetOpcodeHandler(opcodeNumber))
    {
        printf("Tried to override handler of %s with %s (opcode %u)", _internalTable[opcodeNumber]->Name, name, opcodeNumber);
        return;
    }

    _internalTable[opcodeNumber] = new OpcodeHandler(opcodeNumber, name);
}

